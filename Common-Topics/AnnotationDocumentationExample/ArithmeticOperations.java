package com.nt.docs;

// AnnotationDocumentationExample

/**
 * Performs Arithmetic Operations like <b> sum, mul, div, sub </b> and etc...
 * @author atharva
 * @since 1.0
 * @see java.lang.Math 
 */

public class ArithmeticOperations{
    
    /**
     * <b> Perform sum operation </b>
     * @param a first value
     * @param b second value
     * @return the sum of the given two values
     */
    public int sum(int a, int b) {
        return a+b;
    }
}